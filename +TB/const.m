            'theta0',   struct('d','Electrode Stoichiometry at 0% SOC','l','\theta_{0}','u','unitless'), ...
            'theta100', struct('d','Electrode Stoichiometry at 100% SOC','l','\theta_{100}','u','unitless'), ...
            'sigma',    struct('d','Lumped Solid Phase Conductivity','l','\bar{\sigma}','u','1/Ohm'), ...
            'kappa',    struct('d','Lumped Electrolyte Phase Conductivity','l','\bar{\kappa}','u','1/Ohm'), ...

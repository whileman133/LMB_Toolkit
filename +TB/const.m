        ); % LUMPED_PARAMETER_METADATA
    end

    methods(Static)
        function f = f(TdegC)
            %F Compute f=F/(R*T) for the supplied T in [C].
            if ~exist('T','var')
                TdegC = 25;
            end
            f = TB.const.F/TB.const.R/(TdegC+273.15);
        end
    end

end